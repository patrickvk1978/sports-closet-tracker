export function getLeagueTeamColorBank(league) {
  if (league === "nfl") return NFL_TEAM_COLOR_BANK;
  return NBA_TEAM_COLOR_BANK;
}

export function getTeamPalette(league, teamOrId) {
  const bank = getLeagueTeamColorBank(league);
  if (teamOrId && typeof teamOrId === "object") {
    const byId = bank[teamOrId.id];
    if (byId) return byId;
    const abbreviationKey = String(teamOrId.abbreviation ?? "").toLowerCase();
    if (abbreviationKey && bank[abbreviationKey]) return bank[abbreviationKey];
  }
  if (typeof teamOrId === "string" && bank[teamOrId]) {
    return bank[teamOrId];
  }
  return DEFAULT_TEAM_COLOR;
}
